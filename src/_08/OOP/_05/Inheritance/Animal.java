package _08.OOP._05.Inheritance;

public class Animal {

    protected String type; //Using "protected" keyword allows any subclass assess this field
    private String size;
    private double weight;

    public Animal(String type, String size, double weight) {
        this.type = type;
        this.size = size;
        this.weight = weight;
    }

    @Override
    public String toString() {
        return "Animal{" +
                "type='" + type + '\'' +
                ", size='" + size + '\'' +
                ", weight=" + weight +
                '}';
    }

    public void move(String speed) {
        System.out.println(type + " moves " + speed);
    }

    public void eat() {
        System.out.println(type + " wants to eat");
    }
}
